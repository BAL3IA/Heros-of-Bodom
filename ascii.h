class ASCII {
public: 
    std::string asciiArt {R"()"}; 
    ASCII () = default;
    ASCII (const std::string& ascii)
        : asciiArt(ascii) {}
    ~ASCII () {}
    
    std::string getAscii() {
        return this -> asciiArt; }

    friend std::ostream& operator <<(std::ostream& obj, const ASCII& asc); 

};

// overloads
std::ostream& operator <<(std::ostream& obj, const ASCII& asc) {
    obj << asc.asciiArt;
    return obj;
}

// função para retornar linhas em branco
void linhasExtras (const int& num) {
    for (int i=0; i<num -1; i++) std::cout << "\n";
    std::cout << std::endl; }

